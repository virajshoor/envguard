#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const { parseArgs } = require('node:util');
const chalk = require('chalk');
const { parseEnvFile, parseSchemaFile } = require('../lib/parser');
const { validate, isBoolean, isEmail, isInteger, isNumber, isPort, isUrl } = require('../lib/validator');
const { formatResults, formatSummary } = require('../lib/reporter');

const DEFAULT_ENV_FILE = '.env';
const DEFAULT_SCHEMA_FILE = '.env.schema';

function printHelp() {
  console.log(`
${chalk.bold('envguard')} validates .env files against a .env.schema file.

${chalk.bold('Usage')}
  envguard check [--env .env.production] [--schema .env.schema]
  envguard check --strict --json
  envguard init [--schema .env.schema] [--from .env]

${chalk.bold('Commands')}
  check   Validate an env file
  init    Create a starter schema

${chalk.bold('Options')}
  --env       Env file to validate (default: .env)
  --schema    Schema file to use or create (default: .env.schema)
  --from      Generate a starter schema from an existing env file
  --strict    Fail when the env file contains keys missing from the schema
  --json      Print machine-readable JSON instead of the table output
  --help      Show this help
`);
}

function starterSchema() {
  return [
    '# .env.schema',
    '# Format: KEY:type:required|optional[:description]',
    '# Optional regex: KEY:type:required:description:/pattern/',
    '# Conditional rule: @require-if:KEY=value:TARGET[:reason]',
    '',
    'NODE_ENV:enum(development,test,production):required:Application environment',
    'PORT:port:required:HTTP server port',
    'DATABASE_URL:url:required:Database connection URL',
    'ADMIN_EMAIL:email:optional:Admin contact email',
    'FEATURE_FLAGS:boolean:optional:Enable feature flags',
    '@require-if:NODE_ENV=production:SENTRY_DSN:SENTRY_DSN is required in production',
    ''
  ].join('\n');
}

function looksLikeJson(value) {
  const trimmed = value.trim();
  if (!trimmed.startsWith('{') && !trimmed.startsWith('[')) {
    return false;
  }

  try {
    JSON.parse(trimmed);
    return true;
  } catch (_error) {
    return false;
  }
}

function inferType(key, value) {
  if (isBoolean(value)) {
    return 'boolean';
  }

  if (isUrl(value)) {
    return 'url';
  }

  if (isEmail(value)) {
    return 'email';
  }

  if (/_?PORT$|PORT_/.test(key) && isPort(value)) {
    return 'port';
  }

  if (isInteger(value)) {
    return 'integer';
  }

  if (isNumber(value)) {
    return 'float';
  }

  if (looksLikeJson(value)) {
    return 'json';
  }

  return 'string';
}

function schemaFromEnv(env) {
  return [
    '# .env.schema',
    '# Generated by envguard init --from',
    '',
    ...Object.entries(env).map(([key, value]) => {
      return `${key}:${inferType(key, value)}:required:Inferred from env file`;
    }),
    ''
  ].join('\n');
}

function cleanResult(result) {
  return {
    key: result.key,
    type: result.type,
    required: result.required,
    pass: result.pass,
    reason: result.reason
  };
}

function jsonSummary(results) {
  const failed = results.filter((result) => !result.pass).length;
  const passed = results.length - failed;

  return {
    valid: failed === 0,
    summary: {
      passed,
      failed,
      total: results.length
    },
    results: results.map(cleanResult)
  };
}

function resolveFile(filePath) {
  return path.resolve(process.cwd(), filePath);
}

function runInit(args) {
  const schemaPath = resolveFile(args.schema || DEFAULT_SCHEMA_FILE);
  const envPath = args.from ? resolveFile(args.from) : null;

  if (fs.existsSync(schemaPath)) {
    console.error(chalk.red('Schema already exists:'), schemaPath);
    process.exitCode = 1;
    return;
  }

  if (envPath && !fs.existsSync(envPath)) {
    console.error(chalk.red('Env file not found:'), envPath);
    process.exitCode = 1;
    return;
  }

  try {
    const content = envPath ? schemaFromEnv(parseEnvFile(envPath)) : starterSchema();
    fs.writeFileSync(schemaPath, content, 'utf8');
  } catch (error) {
    console.error(chalk.red('Could not create schema:'), error.message);
    process.exitCode = 1;
    return;
  }

  console.log(chalk.green('Created'), schemaPath);
}

function runCheck(args) {
  const envPath = resolveFile(args.env || DEFAULT_ENV_FILE);
  const schemaPath = resolveFile(args.schema || DEFAULT_SCHEMA_FILE);

  if (!fs.existsSync(envPath)) {
    console.error(chalk.red('Env file not found:'), envPath);
    process.exitCode = 1;
    return;
  }

  if (!fs.existsSync(schemaPath)) {
    console.error(chalk.red('Schema file not found:'), schemaPath);
    console.error(chalk.gray('Run `envguard init` to create a starter schema.'));
    process.exitCode = 1;
    return;
  }

  try {
    const env = parseEnvFile(envPath);
    const schema = parseSchemaFile(schemaPath);
    const results = validate(env, schema, { strict: args.strict });
    const failures = results.filter((result) => !result.pass);

    if (args.json) {
      console.log(JSON.stringify(jsonSummary(results), null, 2));
    } else {
      console.log(formatResults(results));
      console.log(formatSummary(results));
    }

    if (failures.length > 0) {
      process.exitCode = 1;
    }
  } catch (error) {
    console.error(chalk.red('Validation failed:'), error.message);
    process.exitCode = 1;
  }
}

function main(argv) {
  let parsed;
  try {
    parsed = parseArgs({
      args: argv,
      allowPositionals: true,
      options: {
        env: { type: 'string' },
        schema: { type: 'string' },
        from: { type: 'string' },
        strict: { type: 'boolean', default: false },
        json: { type: 'boolean', default: false },
        help: { type: 'boolean', short: 'h', default: false }
      }
    });
  } catch (error) {
    console.error(chalk.red('Invalid arguments:'), error.message);
    printHelp();
    process.exitCode = 1;
    return;
  }

  const args = parsed.values;
  const command = parsed.positionals[0];

  if (args.help || !command) {
    printHelp();
    return;
  }

  if (command === 'check') {
    runCheck(args);
    return;
  }

  if (command === 'init') {
    runInit(args);
    return;
  }

  console.error(chalk.red('Unknown command:'), command);
  printHelp();
  process.exitCode = 1;
}

main(process.argv.slice(2));
